class Nightmare extends Dream {
    @Override
    void imagine() {
        System.out.println("Imagining a scary situation.");
    }

    @Override
    void visualize() {
        System.out.println("Visualizing monsters under the bed.");
    }

    @Override
    void aspire() {
        System.out.println("Aspiring to overcome fear.");
    }

    @Override
    void setGoal() {
        System.out.println("Setting a goal to stay strong.");
    }

    @Override
    void reflect() {
        System.out.println("Reflecting on courage gained.");
    }
}