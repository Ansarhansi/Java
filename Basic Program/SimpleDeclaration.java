public class SimpleDeclaration {
    public static void main(String[] args) {
        int a = 5;
        char c='j';
        float f = 12.0f;
        double d = 24.00;
        String s = "I like Programming Language";
        short sh = 12345;
        long li = 2345678L;
        boolean b = true;

        System.out.println("Java program with all declaration variables of primitive data types are:");
        System.out.println("The value stored in the int datatype is: " + a);
        System.out.println("The value stored in the char array is: " +c);
        System.out.println("The value stored in the float datatype is: " + f);
        System.out.println("The value stored in the double datatype is: " + d);
        System.out.println("The value stored in the String datatype is: " + s);
        System.out.println("The value stored in the short datatype is: " + sh);
        System.out.println("The value stored in the long datatype is: " + li);
        System.out.println("The value stored in the boolean datatype is: " + b);
    }
}